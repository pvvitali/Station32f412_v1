/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */


	receiveUart1Byte = LL_USART_ReceiveData8(USART1);
	flag_is_receive = 1;
	//
	simReadBuff[countSimReadBuff] = receiveUart1Byte;
	countSimReadBuff++;
	if(countSimReadBuff > 48) countSimReadBuff = 0;	//49 is '\0' never change


  /* USER CODE END USART1_IRQn 0 */
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

/**
  * @brief This function handles USART3 global interrupt.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */

//	uint8_t d = USART3->DR; // получить данное
//	while ((USART6->SR & USART_SR_TXE) == 0) {}
//	USART6->DR = d; // отослать данное назад

	uint8_t d = LL_USART_ReceiveData8(USART3);
	while (!LL_USART_IsActiveFlag_TXE(USART6)) {}
	LL_USART_TransmitData8(USART6, d);

  /* USER CODE END USART3_IRQn 0 */
  /* USER CODE BEGIN USART3_IRQn 1 */

  /* USER CODE END USART3_IRQn 1 */
}

/**
  * @brief This function handles USART6 global interrupt.
  */
void USART6_IRQHandler(void)
{
  /* USER CODE BEGIN USART6_IRQn 0 */

//	uint8_t d = USART6->DR; // получить данное
//	while ((USART3->SR & USART_SR_TXE) == 0) {}
//	USART3->DR = d; // отослать данное назад

	uint8_t d = LL_USART_ReceiveData8(USART6);
	Dwin_receive_data(d, 0x5012, &pwm_value);
	if( Dwin_receive_data_2(d, 0x5017, &on_off_accum_value) == 1 ){
		forse_write_eeprom = 1;
	}
	while (!LL_USART_IsActiveFlag_TXE(USART3)) {}
	LL_USART_TransmitData8(USART3, d);


  /* USER CODE END USART6_IRQn 0 */
  /* USER CODE BEGIN USART6_IRQn 1 */

  /* USER CODE END USART6_IRQn 1 */
}

